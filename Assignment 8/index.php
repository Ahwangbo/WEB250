echo '<title>Storm Data Analyzer</title>';
echo '<style>';
echo 'body {';
echo 'color: blue;';
echo 'background-color: aqua;';
echo 'font-family: sans-serif;';
echo 'margin: 20px;';
echo 'text-align: center;';
echo '}';
echo 'table {';
